 'def preprocess_steps(gray):\n'
 '    """Return an ordered dict of intermediate images, one per cleaning step."""\n'
 '    # 1. Illumination flattening: divide out a heavily-blurred background estimate to\n'
 '    #    cancel the uneven raking-light gradient typical of squeezes.\n'
 '    bg = cv2.GaussianBlur(gray, (0, 0), sigmaX=41)\n'
 "    flat = cv2.divide(gray, bg, scale=255).astype('uint8')\n"
