 '# === Character n-gram LM for shallow-fusion rescoring (Phase 3, item 9) ===\n'
 '# Inscriptions are formulaic, so a small char-level LM over the corpus transcripts can\n'
 '# fix single-character and line-length slips. We train an interpolated (Jelinek-Mercer)\n'
 "# char n-gram on the TRAIN+VAL row transcripts (no test leakage), then rescore TrOCR's\n"
 '# top-N beams by  mean_logp_TrOCR + w * mean_logp_LM  (w tuned on val in the next cell).\n'
 '# trocr_decode() generalizes the decode helpers to optionally perform this rerank.\n'
 'import math\n'
 'from collections import Counter, defaultdict\n'
 '\n'
 '\n'
 'class CharNGramLM:\n'
 '    def __init__(self, order=6, lam=0.4):\n'
 '        self.order, self.lam = order, lam\n'
 '        self.counts = [defaultdict(Counter) for _ in range(order + 1)]   # counts[m]: ctx(len m-1)->next char\n'
 '        self.vocab = set()\n'
 '\n'
 '    def train(self, lines):\n'
 '        for ln in lines:\n'
 '            if not ln:\n'
 '                continue\n'
 '            self.vocab.update(ln)\n'
 '            for i, ch in enumerate(ln):\n'
 '                for m in range(1, self.order + 1):\n'
 '                    ctx = ln[i - (m - 1):i]\n'
 '                    if len(ctx) == m - 1:\n'
 '                        self.counts[m][ctx][ch] += 1\n'
 '        return self\n'
 '\n'
 '    def cond_logprob(self, ch, context):\n'
 '        p = 1.0 / max(len(self.vocab), 1)                # order-0 uniform base\n'
 '        for m in range(1, self.order + 1):\n'
 "            ctx = context[-(m - 1):] if m > 1 else ''\n"
 '            cnt = self.counts[m].get(ctx)\n'
 '            ph = (cnt.get(ch, 0) / sum(cnt.values())) if cnt else 0.0\n'
 '            p = self.lam * ph + (1 - self.lam) * p\n'
 '        return math.log(max(p, 1e-12))\n'
 '\n'
 '    def mean_logprob(self, text):\n'
 '        if not text:\n'
 '            return 0.0\n'
 '        return sum(self.cond_logprob(text[i], text[:i]) for i in range(len(text))) / len(text)\n'
 '\n'
