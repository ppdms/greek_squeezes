 '# --- Optional alphabet-constrained decoding -----------------------------------\n'
 "# The proxy targets use a small alphabet (the 24 Greek->Latin proxy letters + ':').\n"
 "# TrOCR's subword vocab can otherwise emit digits, punctuation or foreign letters, so we\n"
 '# can mask the decoder logits to a legal token set. Two modes:\n'
 "#   'charset'    : allow any token whose characters are all in the alphabet (+ space).\n"
 "#   'singlechar' : allow only single-character tokens -> forces char-by-char decoding,\n"
 '#                  which matches the scriptio-continua target most closely.\n'
 'class AllowedTokensLogitsProcessor(LogitsProcessor):\n'
 '    def __init__(self, allowed_ids, vocab_size, device):\n'
 "        mask = torch.full((vocab_size,), float('-inf'))\n"
 '        mask[torch.tensor(sorted(allowed_ids), dtype=torch.long)] = 0.0\n'
 '        self.mask = mask.to(device)\n'
 '\n'
 '    def __call__(self, input_ids, scores):\n'
 '        return scores + self.mask\n'
 '\n'
 '\n'
 "def build_allowed_token_ids(allowed_chars, mode='charset'):\n"
 '    """Token ids whose decoded text is legal under `mode` (special tokens always kept)."""\n'
 '    tok = processor.tokenizer\n'
 '    allowed = set(c.upper() for c in allowed_chars)\n'
 '    ids = set(int(i) for i in tok.all_special_ids if i is not None)\n'
 '    for tid in range(len(tok)):\n'
 '        core = tok.decode([tid], skip_special_tokens=True).strip()\n'
 "        if core == '':\n"
 '            ids.add(tid)                      # empty/space tokens; spaces stripped at scoring\n'
 '            continue\n'
 '        if not all(c.upper() in allowed for c in core):\n'
 '            continue\n'
 "        if mode == 'singlechar' and len(core) != 1:\n"
 '            continue\n'
 '        ids.add(tid)\n'
 '    return sorted(ids)\n'
