 '        xord = np.argsort(xcent)\n'
 '        xlut = np.argsort(xord)\n'
 '    \n'
 '        # find left and right nearest neighbors for each box\n'
 '        for i in range(nboxes):\n'
 '            ci = boxes[i].center()\n'
 '            if (gangle==None):\n'
 '                # no global angle so compute a local one from the top line of this box\n'
 '                angle = boxes[i].angle()\n'
 '            else:\n'
 '                angle = gangle\n'
 '            ax = math.cos(angle)\n'
 '            ay = math.sin(angle)\n'
 '            rd = np.inf  # current distance to closest right leighbor\n'
 '            ld = np.inf  # current distance to closest left neighbor\n'
 '            # we are going to start at the x value of the current box, and check nearby\n'
 '            # values of x both higher and lower\n'
 '            klo = xlut[i]\n'
 '            khi = klo\n'
 '            assert(ci[0] == xsort[klo])\n'
 '            while min(ci[0]-xsort[klo],xsort[khi]-ci[0]) < max(rd,ld) and (klo>0 or khi < nboxes-1):\n'
 '                if (ci[0]-xsort[klo] < xsort[khi]-ci[0] or khi >= nboxes-1) and klo > 0:\n'
 '                    # reduce klo\n'
 '                    klo = klo-1\n'
