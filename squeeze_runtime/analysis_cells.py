 'def dist2seg(x,y,seg):\n'
 "    '''\n"
 '    # compute the distance from a point to the nearest point on a line segment\n'
 "    '''\n"
 '    x0 = seg[0]\n'
 '    y0 = seg[1]\n'
 '    x1 = seg[2]\n'
 '    y1 = seg[3]\n'
 '    rx = x-x0\n'
 '    ry = y-y0\n'
 '    rx1 = x1-x0\n'
 '    ry1 = y1-y0\n'
 '    seglen = math.sqrt(rx1**2+ry1**2)\n'
 '    parcomp = (rx*rx1+ry*ry1)/seglen\n'
 '    if parcomp<0:\n'
 '        # closest point is p0 endpoint\n'
 '        d = math.sqrt(rx**2+ry**2)\n'
 '    elif parcomp > seglen:\n'
 '        # closest point is p1 endpoint\n'
 '        d = math.sqrt((x-x1)**2+(y-y1)**2)\n'
 '    else:\n'
 '        # find closest point p along segment\n'
 '        t = parcomp/seglen\n'
 '        px = x0+t*rx1\n'
 '        py = y0+t*ry1\n'
 '        d = math.sqrt((x-px)**2+(y-py)**2)\n'
 '    return d\n'
